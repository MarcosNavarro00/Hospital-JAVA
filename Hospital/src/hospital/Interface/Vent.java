                Contenedor contenedor = (Contenedor) ois.readObject();
               
                Queue<Pacientes> colaRecepcion = contenedor.getRecepcion().getColaRecepcion();
                ArrayList<String> lista = contenedor.getDescanso().getArray();
                HashMap<Integer, Puestos> puestos = contenedor.getVacunacion().getMapa();
