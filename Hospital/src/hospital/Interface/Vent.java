    public static void mostrarRecepcion(Queue<Pacientes> colaPacientes){
        //System.out.println("EEEEEEEEEEEEEEE " );
        
         for (int i = 0; i < colaPacientes.size(); i++) {
            Pacientes pa = colaPacientes.poll();
            array.add( pa.getID());
            colaPacientes.offer(pa);
         }
       
        tf.get(0).setText(array.toString());
        array.clear();
        
    }
    public static Sanitarios cerrarPuesto (HashMap <Integer, Puestos> puestos){
        
        
        if (puestoCerrado != -1){
            Puestos puestoAux = (Puestos) puestos.get(puestoCerrado);
            
            if (puestoAux != null){
                
                Sanitarios sani = (Sanitarios) puestoAux.getSanitario();//sanitario que debe pasar a la sala de descanso
                sani.setCerrado(true);
                sani.setSanitario(sani);
                puestoAux.setSanitario(null);
                puestoAux.setActivo(false);
                puestos.put(puestoCerrado,puestoAux);
               
                return sani;
                
            }
        }
        return null;
      
    }
 
